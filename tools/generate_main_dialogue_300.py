def main() -> None:
    base = BASE.read_text(encoding="utf-8").rstrip()
    base = re.sub(r"\n## 300 题增量扩展区[\s\S]*$", "", base).rstrip()

    if len(rows) != 172:
        raise SystemExit(f"增量题数错误：{len(rows)}，期望 172")

    ids = [row[0] for row in rows]
    if len(ids) != len(set(ids)):
        raise SystemExit("增量区存在重复 ID")

    summaries = [row[7] for row in rows]
    if len(summaries) != len(set(summaries)):
        raise SystemExit("增量区存在重复题目/操作摘要")

    duplicated_with_base = [summary for summary in summaries if summary in base]
    if duplicated_with_base:
        raise SystemExit(f"增量题与基础题库重复：{duplicated_with_base[:3]}")

    easy_added = sum(1 for row in rows if row[5] == "是")
    if easy_added != 114:
        raise SystemExit(f"增量易错题数错误：{easy_added}，期望 114")

    header = [
        "",
        "## 300 题增量扩展区",
        "",
        "说明：本节是在现有 128 题基础上追加的 172 道增量用例，保留原题、原 ID 和原有顺序，不另起一套新题库。增量区继续使用 benchmark-aware 设计，并显式标记易错/陷阱题，便于后续抽样和统计。",
        "",
        "| ID | 优先级 | 模块 | 子能力 | 参考/来源 | 易错 | 陷阱类型 | 题目/操作摘要 | 预期回答/判分规则 | App 实际回答 | 响应时间 | 结果 | 证据路径 |",
        "| --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- | --- |",
    ]

    body = [
        "| "
        + " | ".join(clean(value) for value in row)
        + " | 待执行 | 待执行 | 待执行 | 待执行 |"
        for row in rows
    ]

    content = base + "\n" + "\n".join(header + body) + "\n"
    total_rows = len(re.findall(r"^\| MD-", content, flags=re.MULTILINE))
    if total_rows != 300:
        raise SystemExit(f"总题数错误：{total_rows}，期望 300")

    OUT.write_text(content, encoding="utf-8")
    print(f"wrote={OUT}")
    print(f"base_rows=128 added_rows={len(rows)} total_rows={total_rows} added_easy={easy_added}")


